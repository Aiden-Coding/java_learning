package com.itmayiedu;

//游戏整体
public class Person {

	// 头部
	public String head;
	// 体部
	public String body;
	// 尾部
	public String foot;

	public String getHead() {
		return head;
	}

	public void setHead(String head) {
		this.head = head;
	}

	public String getBody() {
		return body;
	}

	public void setBody(String body) {
		this.body = body;
	}

	public String getFoot() {
		return foot;
	}

	public void setFoot(String foot) {
		this.foot = foot;
	}

}
